function setBackgroundColor(elementID) {

    const element = document.getElementById(elementID);
    element.classList.add('bg-orange-400');
}

function removeBackgroundColor(elementID) {

    const element = document.getElementById(elementID);
    element.classList.remove('bg-orange-400');
}

